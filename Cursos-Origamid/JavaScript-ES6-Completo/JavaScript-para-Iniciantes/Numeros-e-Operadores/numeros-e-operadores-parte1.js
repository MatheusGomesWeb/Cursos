// declarando variavel int
let inteiro = 26;

// declarando variavel exponencial
let exponencial = 2e10;

// declarando variavel float
let _float = 1.90;

// imprimindo variavel inteiro
console.log(inteiro);

// imprimindo variavel exponencial
console.log(exponencial);

// imprimindo variavel float
console.log(_float);

// operadores aritméticos - soma
console.log("Soma: " + (inteiro + inteiro));

// operadores aritméticos - subtração
console.log("Subtração: " + (inteiro - inteiro));

// operadores aritméticos - divisão
console.log("Divisão: " + (inteiro / inteiro));

// operadores aritméticos - resto da divisão
console.log("Resto Divisão: " + (_float % _float));