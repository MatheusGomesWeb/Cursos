// Operadores de Atribuição
var x = 5;
var y = 10;
x += y // x = x + y = 15
x -= y // x = x - y = -5
x *= y // x = x * y = 50
x /= y // x = x / y = 0.2
x %= y // x = x % y = 0

// Operador Ternário - Geralmente utilizado para atribuir valor a uma variavel caso uma condição seja verdadeira ou falsa.
let idade = 18;
let podeBeber = (idade >= 18) ? "Pode Beber" : "Não pode Beber";
console.log(podeBeber);
