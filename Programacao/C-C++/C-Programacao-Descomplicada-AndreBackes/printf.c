#include <stdio.h>

int main() {

    char letra = 'A';
    int num = 10;
    float n = 5.25;
    double nd = 5.50;

    /* 
        * %c = imprime tipo CHAR
        * %d = imprime DECIMAL
        * %f = imprime FLOAT e DOUBLE
        * %i = imprime INTEIRO
     */

    printf("%c \n", letra);
    printf("%d \n", letra);
    printf("%d \n", num);
    printf("%f \n", n);
    printf("%f \n", nd);
    printf("%i \n", num);

    return 0;
}