// Funções: Bloco de código onde podemos reaproveitar o mesmo código varias vezes

// Parametros x Argumentos: 
// - Parametro: Variaveis passadas para a função ao CRIA-LA.
// - Argumentos: Valores passados para EXECUTAR a função.
//  - Argumentos tambem podem ser funções.

// Declarando a função quadrado com o parâmetro: lado
function quadrado(lado) {
  return lado * lado;
}

// executando a função quadrado passando o Argumento: 2
console.log(quadrado(2));